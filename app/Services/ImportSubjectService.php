    public function remove($needle = 'roots'){
        $arraySubjectsSearch =$this->arraySubjects
            ->filter(function($item, $key) use ($needle){
                return stripos($item['parents'],$needle) !== false;
            });
        foreach ($arraySubjectsSearch as $key => $subject){
            $parentId = Subject::where('name',$needle)->first('id');
            $subjectNew = Subject::where('name',$subject['subjectid'])->first();
            if(!isset($subjectNew)){
                $subjectNew = new Subject(['name' => $subject['subjectid']]);
            }
            $subjectNew->result = $subject['name_name'];
            $subjectNew->depositable = strcmp("FALSE", $subject['depositable'])?true:false;
            $subjectNew->showable = true;
            $subjectNew->parent_id = $parentId->id;
            $subjectNew->save();
            $this->arraySubjects->forget($key);
            $this->remove($subject['subjectid']);
        }
    }
